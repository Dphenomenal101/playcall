import fs from "node:fs"
import path from "node:path"

const repoRoot = path.resolve(import.meta.dirname, "../..", "..")
const entryFile = "lib/jobs/processors.ts"
const vendorDir = "supabase/functions/process-job/_vendor"
const generatedBanner = "// Generated by supabase/functions/_shared/sync-process-job-vendor.mjs. Do not edit directly.\n"

function parseImports(source) {
  const importPattern =
    /(?:import|export)\s+(?:[^'"`]*?from\s*)?["']([^"']+)["']|import\(\s*["']([^"']+)["']\s*\)/g
  const specifiers = []
  let match

  while ((match = importPattern.exec(source)) !== null) {
    specifiers.push(match[1] ?? match[2])
  }

  return specifiers
}

function resolveProjectImport(fromFile, specifier) {
  if (specifier.startsWith("@/")) {
    return path.join(repoRoot, specifier.slice(2))
  }

  if (specifier.startsWith(".")) {
    return path.resolve(path.dirname(fromFile), specifier)
  }

  return null
}

function resolveExistingFile(candidateBase) {
  const candidates = [
    candidateBase,
    `${candidateBase}.ts`,
    `${candidateBase}.tsx`,
    path.join(candidateBase, "index.ts"),
    path.join(candidateBase, "index.tsx"),
  ]

  return candidates.find((candidate) => fs.existsSync(candidate) && fs.statSync(candidate).isFile()) ?? null
}

function vendorPathForSource(sourceFile) {
  return path.join(repoRoot, vendorDir, path.relative(repoRoot, sourceFile))
}

function rewriteSpecifier(fromFile, specifier) {
  const candidateBase = resolveProjectImport(fromFile, specifier)

  if (!candidateBase) {
    return specifier
  }

  const resolvedTarget = resolveExistingFile(candidateBase)

  if (!resolvedTarget) {
    return specifier
  }

  const fromVendorFile = vendorPathForSource(fromFile)
  const targetVendorFile = vendorPathForSource(resolvedTarget)
  let nextSpecifier = path.relative(path.dirname(fromVendorFile), targetVendorFile).replaceAll(path.sep, "/")

  if (!nextSpecifier.startsWith(".")) {
    nextSpecifier = `./${nextSpecifier}`
  }

  return nextSpecifier
}

const visitedFiles = new Set()

function collectDependencyGraph(relativeFile) {
  const absoluteFile = path.join(repoRoot, relativeFile)

  if (visitedFiles.has(absoluteFile)) {
    return
  }

  visitedFiles.add(absoluteFile)

  const source = fs.readFileSync(absoluteFile, "utf8")

  for (const specifier of parseImports(source)) {
    const candidateBase = resolveProjectImport(absoluteFile, specifier)

    if (!candidateBase) {
      continue
    }

    const resolvedTarget = resolveExistingFile(candidateBase)

    if (resolvedTarget) {
      collectDependencyGraph(path.relative(repoRoot, resolvedTarget))
    }
  }
}

function rewriteFile(sourceFile) {
  const source = fs.readFileSync(sourceFile, "utf8")

  return (
    generatedBanner +
    source.replace(
      /((?:import|export)\s+(?:[^'"`]*?from\s*)?["'])([^"']+)(["'])|((?:import)\(\s*["'])([^"']+)(["']\s*\))/g,
      (match, staticPrefix, staticSpecifier, staticSuffix, dynamicPrefix, dynamicSpecifier, dynamicSuffix) => {
        if (staticSpecifier !== undefined) {
          return `${staticPrefix}${rewriteSpecifier(sourceFile, staticSpecifier)}${staticSuffix}`
        }

        if (dynamicSpecifier !== undefined) {
          return `${dynamicPrefix}${rewriteSpecifier(sourceFile, dynamicSpecifier)}${dynamicSuffix}`
        }

        return match
      }
    )
  )
}

collectDependencyGraph(entryFile)

for (const sourceFile of [...visitedFiles].sort()) {
  const destinationFile = vendorPathForSource(sourceFile)
  fs.mkdirSync(path.dirname(destinationFile), { recursive: true })
  fs.writeFileSync(destinationFile, rewriteFile(sourceFile))
}

console.log(`Synced ${visitedFiles.size} files into ${vendorDir}`)
